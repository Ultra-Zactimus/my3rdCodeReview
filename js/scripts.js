function mrRoboto(input) {
  const roboLingo = 'beep';
  const roboLingua = 'boop';
  const roboSwag = "Won't you be my neighbor?";
  if (input === 0) {
    return '0';
  } else if (input === 1) {
    return roboLingo;
  } else if (input === 2) {
    return roboLingua;
  } else if (input === 3) {
    return roboSwag;
  }
}
mrRoboto();
